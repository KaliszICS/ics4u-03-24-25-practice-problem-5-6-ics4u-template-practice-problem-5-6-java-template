public class Wheels {
    private int numberOfWheels;
    private int size;

    public Wheels(int numberOfWheels, int size) {
        this.numberOfWheels = numberOfWheels;
        this.size = size;
    }

    public int getNumberOfWheels() {
        return numberOfWheels;
    }

    public int getSize() {
        return size;
    } 
}